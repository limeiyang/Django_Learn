def random_name():
    # 取得随机的姓
    xing = random.choice(XING)
    # 取得随机的性别
    sex = random.choice(['male', 'female'])
    # 取得随机的名
    ming = random.choice(MING[sex])
    # 合并姓名
    name = xing + ming

    return name, sex

